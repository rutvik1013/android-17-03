// WAP to find max value using function as a express.
fun main()
{
    var n1 = 12
    var n2 = 12
    var n3 = 10

    if(n1>=n2 && n1>=n3)
    {
        println("$n1 is Max")
    }
    else if(n2>=n1 && n2>=n3)
    {
        println("$n2 is Max")
    }
    else
    {
        println("$n3 is Max")
    }
}
