fun main()
{
    // Area of Rectangle
    var w=12
    var l=5

    var rectangle =w*l

    if(rectangle>0)
    {
        println("Area of Rectangle:$rectangle")
    }
    // Area of Triangle

    var b=14
    var h=10
    var triangle=b*h/2

    if(triangle>0)
    {
        println("Area of triangle:$triangle")
    }
    // Area of circle

    var pi=3.14
    var radius=5.2
    var circle=pi*radius*radius

    if(circle>0)
    {
        println("Area of Circle:$circle")
    }
}