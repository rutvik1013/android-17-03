//write a program to find out the Square and cube of given number using function.
fun square(s:Int):Int
{


    println("Area of Square is:$s ")
    return s*s
}
fun cube(a:Int):Int
{

    println("Area of cube is:$a")
    return a*a*a
}
fun main()
{
    println(square(6))
    println(cube(4))
}